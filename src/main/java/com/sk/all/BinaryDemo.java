package com.sk.all;

public class BinaryDemo {

	public static void main(String[] args) {
		System.out.println("Binary is " + Integer.toBinaryString(-2));
		System.out.println("Binary is " + Integer.toBinaryString(-1)); 
		
		System.out.println("Binary add operation is" +Integer.toBinaryString(-2 + -1));
		System.out.println("Binary & operation is" +Integer.toBinaryString(-2 & -1));
		
	}

}
